CREATE SCHEMA MOMENTUM_TOURNAMENT;

CREATE TABLE MOMENTUM_TOURNAMENT.TEAMS (
ID SERIAL PRIMARY KEY NOT NULL, 
NAME VARCHAR(255) NOT NULL, 
COLOR VARCHAR(255) NOT NULL);

CREATE TABLE MOMENTUM_TOURNAMENT.PLAYERS (
ID SERIAL PRIMARY KEY NOT NULL, 
FIRST_NAME VARCHAR(255) NOT NULL, 
LAST_NAME VARCHAR(255) NOT NULL, 
PHOTO_PATH VARCHAR(255), 
IS_CAPTAIN BOOLEAN NOT NULL, 
TEAM_ID INTEGER NOT NULL REFERENCES MOMENTUM_TOURNAMENT.TEAMS(ID));

CREATE TABLE MOMENTUM_TOURNAMENT.SINGLE_GAME (
ID SERIAL PRIMARY KEY NOT NULL,
EVENT_DATE DATE NOT NULL, 
FIRST_PLAYER_ID INTEGER NOT NULL REFERENCES MOMENTUM_TOURNAMENT.PLAYERS(ID), 
FIRST_PLAYER_TOTAL_POINTS INTEGER, 
SECOND_PLAYER_ID INTEGER NOT NULL REFERENCES MOMENTUM_TOURNAMENT.PLAYERS(ID), 
SECOND_PLAYER_TOTAL_POINTS INTEGER NOT NULL);

CREATE TABLE MOMENTUM_TOURNAMENT.DOUBLE_GAME (
ID SERIAL PRIMARY KEY NOT NULL,
EVENT_DATE DATE NOT NULL, 
FIRST_PAIR_FIRST_PLAYER_ID INTEGER NOT NULL REFERENCES MOMENTUM_TOURNAMENT.PLAYERS(ID), 
FIRST_PAIR_SECOND_PLAYER_ID INTEGER NOT NULL REFERENCES MOMENTUM_TOURNAMENT.PLAYERS(ID), 
SECOND_PAIR_FIRST_PLAYER_ID INTEGER NOT NULL REFERENCES MOMENTUM_TOURNAMENT.PLAYERS(ID), 
SECOND_PAIR_SECOND_PLAYER_ID INTEGER NOT NULL REFERENCES MOMENTUM_TOURNAMENT.PLAYERS(ID), 
FIRST_PAIR_TOTAL_POINTS INTEGER NOT NULL, 
SECOND_PAIR_TOTAL_POINTS INTEGER NOT NULL);

CREATE TABLE MOMENTUM_TOURNAMENT.UPDATE_AUTHORITY (
ID SERIAL PRIMARY KEY NOT NULL,
EVENT_DATE DATE NOT NULL, 
CAPTAIN_ID INTEGER NOT NULL REFERENCES MOMENTUM_TOURNAMENT.PLAYERS(ID),
GUID VARCHAR(255) NOT NULL);

